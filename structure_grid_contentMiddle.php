This is my simple retro PHP website made with CSS GRID and FLEXBOX.
</br>
    CSS GRID was used specially for the main layout of the entire page.
    With <code>grid-template-rows: 5%  5% 35% 25% 25% 5%;</code> I create 5 rows and with 
    <code>grid-template-areas:</code> I created 3 columns. 
    Besides I used a simple keyframe <code> @media only screen and (max-width: 600px)</code> to make the website responsive.
</br>
</br>
    Inside the top navigation bars I applied <code>display: flex;</code> to layout horizontally the 
    links and <code>justify-content: flex-end;</code> to position the elements to the left side of the screen.
</br>You may notice that I like scroll bars and I am not really fan of too much blank space.
