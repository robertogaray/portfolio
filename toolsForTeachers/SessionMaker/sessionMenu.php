<h1>CREADOR DE SESIONES PERÚ 2022</h1>
<h2>Acerca de las sesiones</h2>
<P>Durante toda una sesión se puede realizar solamente 1 actividad con varias sesiones. Por motivos de sostenibilidad (asegura las necesidades del presente sin comprometer las necesidades de futuras generaciones.) se optó por esta opción.
<BR>Se tomó cómo el título de las sesiones el título de la actividad y la el título del producto final.
<BR>Por motivos de sostenibilidad los criterios de evaluación se plantearon de la misma manera que los desempeños, dado que los desempeños forman parte final de los criterios de evaluación.
<BR>Por motivos de sostenibilidad las sesiones de tutoría presentan un cuadro para que el docente marque la actividad correspondiente.
<BR>Todas las sesiones se fundamentaron en las bases de AprendoEnCasa 2021 y algunos títulos de las actividades fueron ligeramente modificados, de tal manera que puedan ser reusados en todas las sesiones correspondientes a las experiencias de aprendizaje.
</P>
